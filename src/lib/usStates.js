const BY_CODE = new Map(US_STATES.map((s) => [s.code, s.name]));
const BY_NAME = new Map(US_STATES.map((s) => [s.name.toLowerCase(), s.code]));

// Normalise any state input (full name or abbreviation) to a 2-letter code.
export function toStateCode(value) {
  const v = String(value || '').trim();
  if (!v) return '';
  if (v.length === 2 && BY_CODE.has(v.toUpperCase())) return v.toUpperCase();
  return BY_NAME.get(v.toLowerCase()) || '';
}

export function stateName(code) {
  return BY_CODE.get(String(code || '').toUpperCase()) || '';
}
